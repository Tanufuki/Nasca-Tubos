        public String obtenerRut(int rut)
    {
        int[]digitos = new int[8];
        int[]multiplicar = new int[8];
        int numMultiplicar = 2;
        int ultimo = 7;
        int total = 0;
        int resto= 0;
        int comprobar = 0;
        String cadenaRut = rut+"";
        for(int i=0; i<cadenaRut.length(); i++)
        {
            digitos[i] = Integer.parseInt(cadenaRut.charAt(ultimo)+"");
            ultimo--;
        }
        for(int i=0; i<cadenaRut.length(); i++)
        {
            multiplicar[i] = digitos[i]*numMultiplicar;
            numMultiplicar++;
            if(numMultiplicar==8)
            {
                numMultiplicar = 2;
            }
        }
        for(int i=0; i<cadenaRut.length(); i++)
        {
            total += multiplicar[i];
        }
        resto = total % 11;
        comprobar = 11 - resto;
        
        if(comprobar==11)
        {
            comprobar = 0;
        }else if(comprobar==10)
        {
            return cadenaRut+"-K";
        }
        return  cadenaRut+"-"+comprobar;
    }

